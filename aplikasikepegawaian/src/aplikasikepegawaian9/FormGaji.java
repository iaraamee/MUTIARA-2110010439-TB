         public void loadData(){
      model.setRowCount(0);
        
        
       try {
        Connection c = config.configDB();
        Statement s = c.createStatement();

        String sql = "SELECT * FROM gaji";
        ResultSet r = s.executeQuery(sql);

        while (r.next()) {
            Object[] o = new Object[5];
            o[0] = r.getString("id_gaji");
            o[1] = r.getString("nm_pegawai");
            o[2] = r.getString("jabatan");
            o[3] = r.getString("gapok");
            o[4] = r.getString("uang_makan");

            model.addRow(o);
        }
        r.close();
        s.close();
    } catch (Exception e) {
        System.out.println("Terjadi kesalahan: " + e);
    }
        }

