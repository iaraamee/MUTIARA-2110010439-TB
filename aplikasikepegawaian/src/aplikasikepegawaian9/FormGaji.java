    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try{
          java.sql.Connection conn=(java.sql.Connection)config.configDB();
          String sql="DELETE FROM gaji WHERE id_gaji='"+txtid.getText()+"'";
          java.sql.PreparedStatement pst = conn.prepareStatement(sql);
          pst.executeUpdate();  
          JOptionPane.showMessageDialog(null, "Data Dihapus");
          loadData();
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null,"Data Dihapus Gagal");
            System.out.println(e.getMessage());
        } 
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        KoneksiGaji kp = new KoneksiGaji();
        kp.id_gaji = txtid.getText();
        kp.nm_pegawai = txtnama.getText();
        kp.jabatan = txtjabatan.getText();
        kp.gapok = txtgapok.getText();
        kp.uang_makan = txtuang.getText();
        try{
            kp.simpan();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!");
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        loadData();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try{
            java.sql.Connection conn=(java.sql.Connection)config.configDB();
            //membangun sql untuk memperbarui datanya
            String sql="UPDATE gaji set nm_pegawai='" + txtnama.getText()+
                    "', jabatan='"+txtjabatan.getText()+
                    "', gapok='"+txtgapok.getText()+
                    "', uang_makan='"+txtuang.getText()+
                    "' WHERE id_gaji='"+txtid.getText()+"'";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Edit!");
            loadData();  
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null,"Data Gagal Di Edit!");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void tbgajiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbgajiMouseClicked
        int tabel = tbgaji.getSelectedRow();
        txtid.setText(tbgaji.getValueAt(tabel, 0).toString());
        txtnama.setText(tbgaji.getValueAt(tabel, 1).toString());
        txtjabatan.setText(tbgaji.getValueAt(tabel, 2).toString());
        txtgapok.setText(tbgaji.getValueAt(tabel, 3).toString());
        txtuang.setText(tbgaji.getValueAt(tabel, 4).toString());
    }//GEN-LAST:event_tbgajiMouseClicked

