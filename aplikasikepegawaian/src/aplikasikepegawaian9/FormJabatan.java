    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        KoneksiJabatan kp = new KoneksiJabatan();
        kp.id_jabatan = txtid.getText();
        kp.nm_jabatan = txtnama.getText();
        kp.gapok_jabatan = txtgapok.getText();
        
        try{
            kp.simpan();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!");
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        loadData();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try{
            java.sql.Connection conn=(java.sql.Connection)config.configDB();
            //membangun sql untuk memperbarui datanya
            String sql="UPDATE jabatan set nm_jabatan='" + txtnama.getText()+
                    "', gapok_jabatan='"+txtgapok.getText()+
                    "' WHERE id_jabatan='"+txtid.getText()+"'";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Edit!");
            loadData();  // memperbarui data yang ditampilkan pada tabel
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null,"Data Gagal Di Edit!");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
       try{
          java.sql.Connection conn=(java.sql.Connection)config.configDB();
          String sql="DELETE FROM jabatan WHERE id_jabatan='"+txtid.getText()+"'";
          java.sql.PreparedStatement pst = conn.prepareStatement(sql);
          pst.executeUpdate();  
          JOptionPane.showMessageDialog(null, "Data Dihapus");
          loadData();
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null,"Data Dihapus Gagal");
            System.out.println(e.getMessage());
        } 
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tbjabatanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbjabatanMouseClicked
         int tabel = tbjabatan.getSelectedRow();
         txtid.setText(tbjabatan.getValueAt(tabel, 0).toString());
         txtnama.setText(tbjabatan.getValueAt(tabel, 1).toString());
         txtgapok.setText(tbjabatan.getValueAt(tabel, 2).toString());
         
    }//GEN-LAST:event_tbjabatanMouseClicked

