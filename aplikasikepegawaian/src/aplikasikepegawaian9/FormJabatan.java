    public void loadData(){
      model.setRowCount(0); 
       try {
        Connection c = config.configDB();
        Statement s = c.createStatement();

        String sql = "SELECT * FROM jabatan";
        ResultSet r = s.executeQuery(sql);

        while (r.next()) {
            Object[] o = new Object[5];
            o[0] = r.getString("id_jabatan");
            o[1] = r.getString("nm_jabatan");
            o[2] = r.getString("gapok_jabatan");

            model.addRow(o);
        }
        r.close();
        s.close();
    } catch (Exception e) {
        System.out.println("Terjadi kesalahan: " + e);
    }
        
    }

